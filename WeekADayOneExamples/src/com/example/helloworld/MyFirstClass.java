package com.example.helloworld;

public class MyFirstClass {
	
	
	//this is a single line comment
	/*
	 * this is a multi line comment
	 * woo
	 * java is awesome
	 * right
	 */
	
	public static void main(String[] args) {
		//main method
		//it is the entry point method from which the JVM
		//can run your program.
		
		System.out.println("Hi, guyz!");	
		/*
		 * public - access modifier, we can call this method from
		 * anywhere
		 * 
		 * static - we do not need to create an object for static
		 * methods to run. they can run themselves.
		 * 
		 * void - it does not return anything
		 * 
		 * main - the name of the method
		 * 
		 * String[] args - array of Strings, used for command line
		 * arguments that are passed as strings
		 */
	}
	
	
	/*
	 * naming conventions:
	 * 
	 * (Java is case sensitive!)
	 * 
	 * variables - camelCase, pascalCase
	 * methods - camelCase, generally a verb
	 * classes - TitleCase, named after nouns
	 * interfaces - TitleCase, named after adjectives
	 * packages - lowercase
	 */

}
